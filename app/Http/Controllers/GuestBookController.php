    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $gb = new GuestBook;
        $gb->name = $request->name;
        $gb->subject = $request->subject; 
        $gb->body = $request->body;
        $gb->save();

        Mail::to(env('MAIL_FROM_ADDRESS', 'hello@example.com'))->send(new MailGuestBook($gb));

        Session::flash('message', 'Guest Book has been seen');
        return Redirect::to('/');
    }

